// ── Screen 1: Shapes ──────────────────────────────────────────────────────────

fn draw_shapes(display: &mut Display) {
    let s1 = PrimitiveStyle::with_stroke(Gray4::BLACK, 1);
    let s2 = PrimitiveStyle::with_stroke(Gray4::BLACK, 2);
    let s4 = PrimitiveStyle::with_stroke(Gray4::BLACK, 4);
    let s8 = PrimitiveStyle::with_stroke(Gray4::BLACK, 8);
    let filled = PrimitiveStyle::with_fill(Gray4::BLACK);
    let label = MonoTextStyle::new(&FONT_6X9, Gray4::BLACK);

    // Section labels
    Text::new("Lines (8 directions)", Point::new(10, 16), label)
        .draw(display)
        .unwrap();
    Text::new("Circles (filled & stroked)", Point::new(340, 16), label)
        .draw(display)
        .unwrap();
    Text::new("Stroke widths", Point::new(10, 296), label)
        .draw(display)
        .unwrap();
    Text::new("Triangle", Point::new(340, 296), label)
        .draw(display)
        .unwrap();
    Text::new("Grey levels", Point::new(660, 296), label)
        .draw(display)
        .unwrap();

    // Dividers
    Line::new(Point::new(320, 0), Point::new(320, 540))
        .into_styled(s1)
        .draw(display)
        .unwrap();
    Line::new(Point::new(640, 270), Point::new(640, 540))
        .into_styled(s1)
        .draw(display)
        .unwrap();
    Line::new(Point::new(0, 270), Point::new(960, 270))
        .into_styled(s1)
        .draw(display)
        .unwrap();

    // ── Lines radiating from (155, 155) ──────────────────────────────────────
    let cx = 155i32;
    let cy = 155i32;
    // (dx, dy) at 45° increments, length ≈ 120 (diagonal = 120*0.707 ≈ 85)
    let dirs: [(i32, i32); 8] = [
        (120, 0), (85, 85), (0, 120), (-85, 85),
        (-120, 0), (-85, -85), (0, -120), (85, -85),
    ];
    for (dx, dy) in dirs {
        Line::new(Point::new(cx, cy), Point::new(cx + dx, cy + dy))
            .into_styled(s2)
            .draw(display)
            .unwrap();
    }
    // Centre dot
    Circle::new(Point::new(cx - 4, cy - 4), 8)
        .into_styled(filled)
        .draw(display)
        .unwrap();

    // ── Concentric circles at (480 + 160, 135) = (640-160+160...) ────────────
    // Right of divider at x=320, centre around (490, 140)
    let cc_x = 490i32;
    let cc_y = 140i32;
    let radii: [u32; 5] = [20, 45, 70, 95, 120];
    for (i, r) in radii.iter().enumerate() {
        let r = *r;
        let style = if i % 2 == 0 { filled } else { s2 };
        Circle::new(Point::new(cc_x - r as i32, cc_y - r as i32), r * 2)
            .into_styled(style)
            .draw(display)
            .unwrap();
    }

    // ── Stroke-width rectangles (bottom-left) ─────────────────────────────────
    let styles = [s1, s2, s4, s8];
    let labels = ["1 px", "2 px", "4 px", "8 px"];
    for (i, (st, lbl)) in styles.iter().zip(labels.iter()).enumerate() {
        let y = 300 + i as i32 * 55;
        Rectangle::new(Point::new(50, y), Size::new(240, 40))
            .into_styled(*st)
            .draw(display)
            .unwrap();
        Text::new(lbl, Point::new(55, y + 26), label)
            .draw(display)
            .unwrap();
    }

    // ── Triangle (bottom-centre) ──────────────────────────────────────────────
    // Workaround: embedded-graphics 0.8.2 overflows i32 in ClosedThickSegmentIter
    // for large coordinates with any stroke width. Draw as 3 separate lines instead.
    let tri_a = Point::new(480, 310);
    let tri_b = Point::new(360, 520);
    let tri_c = Point::new(600, 520);
    Line::new(tri_a, tri_b).into_styled(s4).draw(display).unwrap();
    Line::new(tri_b, tri_c).into_styled(s4).draw(display).unwrap();
    Line::new(tri_c, tri_a).into_styled(s4).draw(display).unwrap();

    // ── Grey-level horizontal lines (bottom-right) ────────────────────────────
    let grey_levels: [u8; 8] = [0, 2, 4, 6, 8, 10, 12, 14];
    for (i, luma) in grey_levels.iter().enumerate() {
        let y = 305 + i as i32 * 27;
        let color = Gray4::new(*luma);
        let gs = PrimitiveStyle::with_stroke(color, 4);
        Line::new(Point::new(660, y), Point::new(940, y))
            .into_styled(gs)
            .draw(display)
            .unwrap();
        let mut buf = Buf::<8>::new();
        buf.push_str("0x");
        buf.push_u64(*luma as u64);
        Text::new(buf.as_str(), Point::new(900, y - 2), label)
            .draw(display)
            .unwrap();
    }
}

