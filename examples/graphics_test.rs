// ── String buffer for on-screen number formatting ────────────────────────────

struct Buf<const N: usize> {
    data: [u8; N],
    len: usize,
}

impl<const N: usize> Buf<N> {
    fn new() -> Self {
        Self { data: [0u8; N], len: 0 }
    }

    fn push_str(&mut self, s: &str) {
        for b in s.bytes() {
            if self.len < N {
                self.data[self.len] = b;
                self.len += 1;
            }
        }
    }

    fn push_u64(&mut self, mut n: u64) {
        if n == 0 {
            self.push_str("0");
            return;
        }
        let mut tmp = [0u8; 20];
        let mut i = 20usize;
        while n > 0 && i > 0 {
            i -= 1;
            tmp[i] = b'0' + (n % 10) as u8;
            n /= 10;
        }
        self.push_str(core::str::from_utf8(&tmp[i..]).unwrap_or("?"));
    }

    fn as_str(&self) -> &str {
        core::str::from_utf8(&self.data[..self.len]).unwrap_or("")
    }
}

// ── Shared helpers ────────────────────────────────────────────────────────────

fn wait_for_button(button: &Input, delay: &Delay) {
    while button.is_high() {}
    delay.delay_millis(50);
    while button.is_low() {}
    delay.delay_millis(50);
}

