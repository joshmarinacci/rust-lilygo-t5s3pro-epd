// ── Screen 5: Animation + timing measurement ──────────────────────────────────

fn run_animation(display: &mut Display, button: &Input, delay: &Delay) -> (u64, u64, u64) {
    let large = MonoTextStyle::new(&FONT_10X20, Gray4::BLACK);
    let small = MonoTextStyle::new(&FONT_7X13, Gray4::BLACK);

    // ── Step A: full-screen flush benchmark ──────────────────────────────────
    // fill() marks all 540 rows dirty → flush sends every row
    display.fill(0x0F).unwrap();

    Text::with_alignment("Animation & Partial Refresh", Point::new(480, 40), large, Alignment::Center)
        .draw(display)
        .unwrap();
    Text::with_alignment(
        "The ball moves in a 120-row band. Only those rows are flushed each frame.",
        Point::new(480, 70),
        small,
        Alignment::Center,
    )
    .draw(display)
    .unwrap();
    Text::new("Measuring full-screen flush (all 540 rows)...", Point::new(40, 100), small)
        .draw(display)
        .unwrap();

    println!("Measuring full flush...");
    let t0 = Instant::now();
    display.flush(DrawMode::BlackOnWhite).unwrap();
    let full_flush_ms = t0.elapsed().as_millis();
    println!("Full flush: {}ms", full_flush_ms);

    // ── Step B: measure hardware clear time ───────────────────────────────────
    println!("Measuring hardware clear...");
    let t0 = Instant::now();
    display.clear().unwrap();
    let clear_ms = t0.elapsed().as_millis();
    println!("Hardware clear: {}ms", clear_ms);

    // Draw the static frame (border + labels) after the clear
    Rectangle::new(Point::new(16, 16), Size::new(928, 508))
        .into_styled(PrimitiveStyle::with_stroke(Gray4::BLACK, 2))
        .draw(display)
        .unwrap();
    Text::with_alignment("Animation & Partial Refresh", Point::new(480, 40), large, Alignment::Center)
        .draw(display)
        .unwrap();
    Text::with_alignment(
        "Ball moves across screen (8 frames). Only the ~80 rows it touches are flushed.",
        Point::new(480, 70),
        small,
        Alignment::Center,
    )
    .draw(display)
    .unwrap();

    // Flush static content — only the tainted rows (title band) are sent
    display.flush(DrawMode::BlackOnWhite).unwrap();

    // ── Step C: animation loop (8 frames, partial flush) ──────────────────────
    // flush() resets the framebuffer to white after every frame, so there is no
    // need to draw a white rectangle to erase the previous ball position —
    // just draw the new ball and flush. Only the ~80 rows the circle touches
    // are marked tainted, keeping the partial flush fast.
    const BALL_DIAM: u32 = 80;
    const BAND_Y: i32 = 210;
    const BAND_H: i32 = 120;
    const FRAMES: u32 = 8;
    const START_X: i32 = 60;
    const STEP_X: i32 = 105;

    let mut sum_partial_ms: u64 = 0;

    for frame in 0..FRAMES {
        let ball_x = START_X + frame as i32 * STEP_X;
        let ball_y = BAND_Y + (BAND_H - BALL_DIAM as i32) / 2;

        // Draw ball — framebuffer is already white after the previous flush()
        Circle::new(Point::new(ball_x, ball_y), BALL_DIAM)
            .into_styled(PrimitiveStyle::with_fill(Gray4::BLACK))
            .draw(display)
            .unwrap();

        // Frame label inside the band
        let mut lbl = Buf::<16>::new();
        lbl.push_str("Frame ");
        lbl.push_u64(frame as u64 + 1);
        Text::new(lbl.as_str(), Point::new(810, BAND_Y + 55), small)
            .draw(display)
            .unwrap();

        let t0 = Instant::now();
        display.flush(DrawMode::BlackOnWhite).unwrap();
        let frame_ms = t0.elapsed().as_millis();
        sum_partial_ms += frame_ms;
        println!("frame {}: {}ms (partial)", frame + 1, frame_ms);
    }

    let partial_avg_ms = sum_partial_ms / FRAMES as u64;

    // Draw timing results at the bottom of the animation screen
    {
        let mut line1 = Buf::<64>::new();
        line1.push_str("Full flush (540 rows): ");
        line1.push_u64(full_flush_ms);
        line1.push_str("ms   |   Partial (~80 rows): ");
        line1.push_u64(partial_avg_ms);
        line1.push_str("ms avg");
        Text::with_alignment(line1.as_str(), Point::new(480, 390), small, Alignment::Center)
            .draw(display)
            .unwrap();
    }
    {
        let speedup_x10 = if partial_avg_ms > 0 { full_flush_ms * 10 / partial_avg_ms } else { 0 };
        let mut line2 = Buf::<64>::new();
        line2.push_str("Speedup: ");
        line2.push_u64(speedup_x10 / 10);
        line2.push_str(".");
        line2.push_u64(speedup_x10 % 10);
        line2.push_str("x faster  |  ~80/540 = ~15% of rows updated");
        Text::with_alignment(line2.as_str(), Point::new(480, 420), small, Alignment::Center)
            .draw(display)
            .unwrap();
    }
    Text::with_alignment(
        "Note: ghosting is expected — the waveform does not actively drive pixels to white.",
        Point::new(480, 450),
        MonoTextStyle::new(&FONT_6X9, Gray4::BLACK),
        Alignment::Center,
    )
    .draw(display)
    .unwrap();

    display.flush(DrawMode::BlackOnWhite).unwrap();

    wait_for_button(button, delay);

    (clear_ms, full_flush_ms, partial_avg_ms)
}

// ── Screen 6: Timing summary ──────────────────────────────────────────────────

fn draw_timing(display: &mut Display, clear_ms: u64, full_ms: u64, partial_ms: u64) {
    let heading = MonoTextStyle::new(&FONT_10X20, Gray4::BLACK);
    let body = MonoTextStyle::new(&FONT_9X18, Gray4::BLACK);
    let note = MonoTextStyle::new(&FONT_7X13, Gray4::BLACK);

    Rectangle::new(Point::new(16, 16), Size::new(928, 508))
        .into_styled(PrimitiveStyle::with_stroke(Gray4::BLACK, 2))
        .draw(display)
        .unwrap();

    Text::with_alignment("Refresh Timing Summary", Point::new(480, 60), heading, Alignment::Center)
        .draw(display)
        .unwrap();

    Line::new(Point::new(40, 78), Point::new(920, 78))
        .into_styled(PrimitiveStyle::with_stroke(Gray4::BLACK, 1))
        .draw(display)
        .unwrap();

    let rows: [(&str, u64, &str); 3] = [
        ("Hardware clear (540 rows):", clear_ms, "ms   — drives all pixels black then white x4"),
        ("Full flush     (540 rows):", full_ms,  "ms   — 15-frame waveform, all rows sent"),
        ("Partial flush  (120 rows):", partial_ms, "ms   — 15-frame waveform, 22% of rows (avg)"),
    ];

    for (i, (label, val, suffix)) in rows.iter().enumerate() {
        let y = 130 + i as i32 * 80;
        Text::new(label, Point::new(60, y), note)
            .draw(display)
            .unwrap();

        let mut num = Buf::<32>::new();
        num.push_u64(*val);
        num.push_str(" ");
        num.push_str(suffix);
        Text::new(num.as_str(), Point::new(100, y + 28), body)
            .draw(display)
            .unwrap();
    }

    Line::new(Point::new(40, 370), Point::new(920, 370))
        .into_styled(PrimitiveStyle::with_stroke(Gray4::BLACK, 1))
        .draw(display)
        .unwrap();

    // Speedup ratio
    let speedup_x10 = if partial_ms > 0 { full_ms * 10 / partial_ms } else { 0 };
    let mut speed_line = Buf::<64>::new();
    speed_line.push_str("Partial refresh is ");
    speed_line.push_u64(speedup_x10 / 10);
    speed_line.push_str(".");
    speed_line.push_u64(speedup_x10 % 10);
    speed_line.push_str("x faster than a full flush.");
    Text::with_alignment(speed_line.as_str(), Point::new(480, 410), heading, Alignment::Center)
        .draw(display)
        .unwrap();

    Text::with_alignment(
        "Rows skipped: 420 of 540 per waveform frame (22% updated, 78% skipped via CKV pulse).",
        Point::new(480, 450),
        note,
        Alignment::Center,
    )
    .draw(display)
    .unwrap();

    Text::with_alignment(
        "Press BOOT to restart.",
        Point::new(480, 510),
        note,
        Alignment::Center,
    )
    .draw(display)
    .unwrap();
}

// ── Main ──────────────────────────────────────────────────────────────────────

#[main]
fn main() -> ! {
    esp_println::logger::init_logger_from_env();

    let config = esp_hal::Config::default()
        .with_cpu_clock(esp_hal::clock::CpuClock::_240MHz);
    let peripherals = esp_hal::init(config);

    let psram_config = esp_hal::psram::PsramConfig {
        mode: esp_hal::psram::PsramMode::OctalSpi,
        ..Default::default()
    };
    esp_alloc::psram_allocator!(peripherals.PSRAM, esp_hal::psram, psram_config);

    let button = Input::new(
        peripherals.GPIO0,
        InputConfig::default().with_pull(Pull::Up),
    );

    let mut display = Display::new(
        epaper::pin_config!(peripherals),
        peripherals.DMA_CH0,
        peripherals.LCD_CAM,
        peripherals.RMT,
        peripherals.I2C0,
    )
    .expect("display init");

    let delay = Delay::new();
    delay.delay_millis(100);
    display.power_on();
    delay.delay_millis(10);

    println!("Graphics test — 6 screens. Press BOOT (GPIO0) to advance.");

    // Measure hardware clear time once at start
    println!("Measuring hardware clear...");
    let t0 = Instant::now();
    display.clear().unwrap();
    let initial_clear_ms = t0.elapsed().as_millis();
    println!("Hardware clear: {}ms", initial_clear_ms);

    // ── Screen 0: Title ───────────────────────────────────────────────────────
    draw_title(&mut display);
    println!("Flushing title...");
    display.flush(DrawMode::BlackOnWhite).unwrap();
    wait_for_button(&button, &delay);

    // ── Screen 1: Shapes ──────────────────────────────────────────────────────
    display.clear().unwrap();
    draw_shapes(&mut display);
    println!("Flushing shapes...");
    display.flush(DrawMode::BlackOnWhite).unwrap();
    wait_for_button(&button, &delay);

    // ── Screen 2: Typography ──────────────────────────────────────────────────
    display.clear().unwrap();
    draw_typography(&mut display);
    println!("Flushing typography...");
    display.flush(DrawMode::BlackOnWhite).unwrap();
    wait_for_button(&button, &delay);

    // ── Screen 3: Grayscale ───────────────────────────────────────────────────
    display.clear().unwrap();
    draw_grayscale(&mut display);
    println!("Flushing grayscale...");
    display.flush(DrawMode::BlackOnWhite).unwrap();
    wait_for_button(&button, &delay);

    // ── Screen 4: Image ───────────────────────────────────────────────────────
    display.clear().unwrap();
    draw_image(&mut display);
    println!("Flushing image...");
    display.flush(DrawMode::BlackOnWhite).unwrap();
    wait_for_button(&button, &delay);

    // ── Screen 5: Animation (also measures full + partial flush time) ─────────
    display.clear().unwrap();
    let (clear_ms, full_flush_ms, partial_avg_ms) =
        run_animation(&mut display, &button, &delay);

    // ── Screen 6: Timing summary ──────────────────────────────────────────────
    display.clear().unwrap();
    draw_timing(&mut display, clear_ms, full_flush_ms, partial_avg_ms);
    println!("Flushing timing summary...");
    display.flush(DrawMode::BlackOnWhite).unwrap();
    println!("Done. clear={}ms full={}ms partial={}ms", clear_ms, full_flush_ms, partial_avg_ms);
    wait_for_button(&button, &delay);

    loop {}
}
